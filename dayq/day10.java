package dayq;

public class day10 {

	public static void main(String[] args) {
		for (int i = 1 ;i <=3 ; i++) {
			System.out.println(i);
		}
		
		
		for( int i = 2; i<=20 ;i++) {
			System.out.println(i);
		}
		
		
		for ( int i = 50 ; i >=5 ; i = i-5) {
			System.out.println(i);
		}
		
		
		
		
		
		for( int i = 3 ; i <=30 ; i = i+3) {
			System.out.println(i);
		}
		
		
		
		
		for (int i = 10 ; i>=1; i--) {
			System.out.println(i);
			if (i ==5 ) {
				break;
			}
		}
		
		
		
		
		for (int i = 10 ; i>=1 ; i--) {
			if (i==4) {
				break;
			}
			System.out.println(i);
		}
	}
	

}
